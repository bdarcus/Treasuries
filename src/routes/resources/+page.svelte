			<p class="text-sm text-slate-600 leading-relaxed">
				A TIPS ladder is a portfolio of individual Treasury Inflation-Protected Securities (TIPS) with staggered maturity dates. As each bond matures, it provides a predictable amount of cash—inflation-adjusted—to fund your spending for that specific year.
			</p>
